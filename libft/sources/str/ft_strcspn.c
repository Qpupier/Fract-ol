#include "../includes/libft.h"

size_t	ft_strcspn(const char *s, const char *reject)
{
	size_t	nb;

	nb = 0;
	while (*s && ((ft_strchr(reject, *s++) == NULL)))
		nb++;
	return (nb);
}
