#include "../includes/libft.h"

t_cplx	ft_cplx_ln(t_cplx z)
{
	t_cplx r;

	r.rl = 0.5 * log(ft_cplx_modul_2(z));
	r.im = atan(z.im / z.rl);
	return (r);
}
